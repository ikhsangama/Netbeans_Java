    private void mahasiswaSelectAll() {
        dlm1.clear();
        dlm2.clear();

        List<Mahasiswa> mhsID = mhsDAO.getAll();
        for (Mahasiswa person : mhsID) {
            dlm1.addElement("" + person.getId() + "");
        }

        List<Mahasiswa> mhsNama = mhsDAO.getAll();
        for (Mahasiswa person : mhsNama) {
            dlm2.addElement(person.getNama());
        }
    }
