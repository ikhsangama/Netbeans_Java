    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        // TODO add your handling code here:
        Mahasiswa mhsAdd = mhsDAO.makeMhsObject();
        mhsAdd.setNama(jTextFieldInsertNama.getText());
        mhsDAO.add(mhsAdd);

        mahasiswaSelectAll();
        jTextFieldInsertNama.setText("");
        jTextFieldDeleteNama.setText("");
        jTextFieldUpdateNama.setText("");
    }//GEN-LAST:event_jButtonInsertActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        Mahasiswa mhsUpd = mhsDAO.makeMhsObject();
        mhsUpd.setId(Integer.parseInt(jTextFieldUpdateID.getText()));
        mhsUpd.setNama(jTextFieldUpdateNama.getText());

        mhsDAO.update(mhsUpd);

        jTextFieldDeleteID.setText("");
        jTextFieldDeleteNama.setText("");
        jTextFieldUpdateID.setText("");
        jTextFieldUpdateNama.setText("");

        mahasiswaSelectAll();
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        Mahasiswa mhsDel = mhsDAO.makeMhsObject();
        mhsDel.setId(Integer.parseInt(jTextFieldDeleteID.getText()));
        mhsDel.setNama(jTextFieldDeleteNama.getText());

        mhsDAO.delete(mhsDel.getId());

        jTextFieldDeleteID.setText("");
        jTextFieldDeleteNama.setText("");
        jTextFieldUpdateID.setText("");
        jTextFieldUpdateNama.setText("");

        mahasiswaSelectAll();
    }//GEN-LAST:event_jButtonDeleteActionPerformed

