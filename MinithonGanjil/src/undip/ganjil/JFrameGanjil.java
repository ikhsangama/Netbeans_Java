    private void jButtonBacaFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBacaFileActionPerformed
        dlmIsiFile.clear();
        isiFile.clear();
        urutkan1.clear();
        isiFile = bacaFile(jTextFieldNamaFile.getText());
        int jumlah = 0;
        for (int i = 0; i < isiFile.size() - 1; i++) {
            try {
                Integer angka = Integer.parseInt(isiFile.get(i)); // ubah menjadi integer
                dlmIsiFile.addElement(angka);
                urutkan1.add(angka);
                jumlah = jumlah + angka;
            } catch (NumberFormatException e) {
                System.out.println("NumberFormatException : " + e.getMessage());
            }
        }
        txtFld_jumlah1.setText("" + jumlah);
    }//GEN-LAST:event_jButtonBacaFileActionPerformed

