    private void jButtonDetekGanjilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetekGanjilActionPerformed
        // TODO add your handling code here:
        int jumlah = 0;
        urutkan2.clear();
        dlmHasilDeteksi.clear();
        for (int i = 0; i < dlmIsiFile.getSize() - 1; i++) {
            if(dlmIsiFile.getElementAt(i)%2!=0){
                int angka = dlmIsiFile.getElementAt(i);
                dlmHasilDeteksi.addElement(angka);
                urutkan2.add(angka);
                jumlah = jumlah + angka;
            }
        }
        txtFld_jml2.setText(""+jumlah);
    }//GEN-LAST:event_jButtonDetekGanjilActionPerformed

